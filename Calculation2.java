/* サブクラスのオブジェクトをスーパークラスのオブジェクトに代入する。
    サブクラスのオブジェクトはスーパークラスのオブジェクトに代入することができる。
    逆は不可。
    
    サブクラスのオブジェクトをスーパークラスのオブジェクトとして代入することで、
    スーパークラスで定義されている抽象メソッドを、サブクラス側で定義(オーバーライド)された、
    処理の内容に基づき実行することが可能となる。
*/

abstract class Calc2 {
    int a;
    int b;

    abstract int result();

    void printResult() {
        System.out.println(result());
    }

    void setData(int m, int n) {
        a = m;
        b = n;
    }
}

class Plus extends Calc2 {
    int result() {return a+b; }
}

class Minus extends Calc2{
    int result() {return a-b; }
}

class Calculation2 {
    public static void main(String[] args) {
        int x = 54, y = 12;
        Calc2 calc1 = new Plus();
        Calc2 calc2 = new Minus();
        calc1.setData(x, y);
        calc2.setData(x, y);
        System.out.print(x + "+" + y + "=");
        calc1.printResult();
        System.out.print(x + "-" + y + "=");
        calc2.printResult();
    }
}